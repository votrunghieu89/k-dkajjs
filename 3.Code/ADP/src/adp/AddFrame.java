    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String tNumA = tfNumA.getText();
        String tNumB = tfNumB.getText();

        if (tNumA.isEmpty() || tNumB.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter all mandatory fields!", "Try again",
                JOptionPane.ERROR_MESSAGE);
        } else {
            double numA, numB;
            if (tNumA.length() > 11 || tNumB.length() > 11) {
                JOptionPane.showMessageDialog(this, "The number must be less than 12 digits", "Number too long",
                        JOptionPane.ERROR_MESSAGE);
                if (tNumA.length() > 11) tfNumA.setText("");
                else if (tNumB.length() > 11) tfNumB.setText("");
                return;
            }
            
            try {
                numA = Double.parseDouble(tfNumA.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Please enter a number!", "Number format",
                    JOptionPane.ERROR_MESSAGE);
                tfNumA.setText("");
                return;
            }

            try {
                numB = Double.parseDouble(tfNumB.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Please enter a number!", "Number format",
                    JOptionPane.ERROR_MESSAGE);
                tfNumB.setText("");
                return;
            }
            
            DecimalFormat dm = new DecimalFormat("#.##");
            double result = numA + numB;
            lbResult.setText((String.valueOf(dm.format(result))));
        }
    }//GEN-LAST:event_btnAddActionPerformed

